const RUBRIC = [
    { category: "Mathematical Essentials", count: 3, description: "Fractions, decimals, percentages, ratios" },
    { category: "Measurement and Conversions", count: 12, description: "Converting between metric, household, and English systems" },
    { category: "Drug Dosages and IV Solutions", count: 21, description: "Calculating dosages, pediatric doses, IV drip rates, time of completion" },
    { category: "Dilutions, Solutions, Concentrations", count: 15, description: "Calculating parts, totals, strengths of solutions" },
    { category: "Interpreting Medical Info", count: 9, description: "Mean, median, mode, standard deviation, body surface area (BSA)" }
];

let allQuestions = [];

document.getElementById('generateBtn').addEventListener('click', generateExam);
document.getElementById('checkAllBtn').addEventListener('click', checkAllAnswers);

async function generateExam() {
    document.getElementById('generateBtn').classList.add('hidden');
    document.getElementById('loadingScreen').classList.remove('hidden');
    document.getElementById('examContainer').classList.add('hidden');
    
    allQuestions = [];
    const questionsList = document.getElementById('questionsList');
    questionsList.innerHTML = '';

    let totalCompleted = 0;

    for (let i = 0; i < RUBRIC.length; i++) {
        const section = RUBRIC[i];
        document.getElementById('loadingStatus').innerText = `Generating ${section.category} (${section.count} Qs)...`;
        
        try {
            const response = await fetch('/api/generate', {
                method: 'POST',
                headers: { 'Content-Type': 'application/json' },
                body: JSON.stringify(section)
            });
            const data = await response.json();
            
            data.questions.forEach((q) => {
                allQuestions.push({ ...q, category: section.category });
            });

            totalCompleted++;
            document.getElementById('progressFill').style.width = `${(totalCompleted / RUBRIC.length) * 100}%`;
            
        } catch (error) {
            console.error("Error generating section", error);
            alert("An error occurred. The AI might be overloaded. Please try again.");
            return;
        }
    }

    renderExam();
    document.getElementById('loadingScreen').classList.add('hidden');
    document.getElementById('examContainer').classList.remove('hidden');
}

function renderExam() {
    const list = document.getElementById('questionsList');
    allQuestions.forEach((q, index) => {
        const card = document.createElement('div');
        card.className = 'question-card';
        card.id = `qcard-${index}`;
        
        card.innerHTML = `
            <h3>Question ${index + 1} <small style="font-size:12px; color:#666;">(${q.category})</small></h3>
            <p>${q.question}</p>
            <div class="input-group">
                <input type="text" id="input-${index}" placeholder="Enter final answer...">
                <button onclick="checkIndividual(${index})">Check</button>
            </div>
            <div id="exp-${index}" class="explanation">
                <strong>Correct Answer:</strong> ${q.final_answer}<br><br>
                <strong>Explanation:</strong> ${q.explanation}
            </div>
        `;
        list.appendChild(card);
    });
}

function checkAnswerLogic(userAnswer, correctAnswer) {
    // Strip spaces and convert to string for safe comparison
    const u = String(userAnswer).trim();
    const c = String(correctAnswer).trim();
    
    // Exact match
    if (u === c) return true;

    // Numerical check (e.g. 15.0 == 15, though HOSA strictly forbids trailing zeroes, 
    // mathematically if they get the number we can count it, but let's be strict for HOSA!)
    if (parseFloat(u) === parseFloat(c) && !c.includes(" ") && !u.includes(" ")) {
        // Strict leading/trailing zero check based on correct answer generated by AI
        if (u !== c) return false; 
    }
    
    return false;
}

function checkIndividual(index) {
    const userInput = document.getElementById(`input-${index}`).value;
    const correctAns = allQuestions[index].final_answer;
    const card = document.getElementById(`qcard-${index}`);
    const exp = document.getElementById(`exp-${index}`);

    exp.style.display = 'block';
    
    if (checkAnswerLogic(userInput, correctAns)) {
        card.classList.add('correct');
        card.classList.remove('incorrect');
        return true;
    } else {
        card.classList.add('incorrect');
        card.classList.remove('correct');
        return false;
    }
}

function checkAllAnswers() {
    let score = 0;
    allQuestions.forEach((q, index) => {
        const isCorrect = checkIndividual(index);
        if (isCorrect) score++;
    });

    const results = document.getElementById('examResults');
    results.innerText = `Final Score: ${score} / 60 (${((score/60)*100).toFixed(1)}%)`;
    window.scrollTo({ top: document.body.scrollHeight, behavior: 'smooth' });
}
